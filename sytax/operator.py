#%% [markdown]
# python 有 许多 运算符
# 
# - 数学运算符
# 
#   |  加   |  减   |  乘   |  除   | 整除  | 求余  |  幂   |
#   | :---: | :---: | :---: | :---: | :---: | :---: | :---: |
#   |   +   |   -   |   *   |   /   |  //   |   %   |  **   |

#%%
a, b = 5, 2

print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")
print(f"{a} / {b} = {a / b}")
print(f"{a} // {b} = {a // b}")
print(f"{a} ** {b} = {a ** b}")
print(f"{a} % {b} = {a % b}")


# %%[markdown]
# 
# - 比较运算符
# 
#   | 等于  | 不等于 | 大于  | 小于  | 小于等于 | 大于等于 |
#   | :---: | :----: | :---: | :---: | :------: | :------: |
#   |  ==   |   !=   |   >   |   <   |    <=    |    >=    |
# 
# %%
print(f"{a} == {b} is {a == b}")
print(f"{a} != {b} is {a != b}")
print(f"{a} > {b} is {a > b}")
print(f"{a} < {b} is {a < b}")
print(f"{a} <= {b} is {a <= b}")
print(f"{a} >= {b} is {a >= b}")
print(f"{a} == {b} is {a == b}")
